function deepMergeTools(base, localeTools) {
  const out = { ...base };
  for (const [key, enVal] of Object.entries(toolsEn)) {
    out[key] = { ...enVal, ...(localeTools?.[key] ?? {}) };
  }
  return out;
}

for (const locale of locales) {
  const file = path.join(root, `${locale}.json`);
  const data = JSON.parse(fs.readFileSync(file, "utf8"));
  const t = translations[locale];
  data.categories = { ...data.categories, ...t.categories };
  data.common = { ...data.common, ...t.common };
  data.tools = deepMergeTools(data.tools, t.tools);
  fs.writeFileSync(file, JSON.stringify(data, null, 2) + "\n");
  console.log("updated", locale);
}
